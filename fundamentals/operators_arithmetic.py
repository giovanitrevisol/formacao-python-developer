"""
ARITHMETIC OPERATORS

- Sum | +
- Subtract | -
- multiplication | *
- Division | /
- Integer Division | //
- Module | %
- exponentiation | **

=================================
Mathematical precedence
1 - Parentheses
2 - Exponents
3 - multiplication and division (from left to right)
4 - sum and subtract (from left to right)
"""

print("Sum")
print(5 + 5)

print("Subtract")
print(6 - 5)

print("Multiplication")
print(6 * 5)

print("Division")
print(40 / 5)

print("Integer Division")
print(41 // 5)

print("Module")
print(43 % 5)

print("exponentiation")
print(2 ** 5)

x = (10 + 5) * 4
y = (10 / 2) + (25 * 2) - (2 ** 3)
print(x)
print(y)
